// 12 perceptually distinct colors for member avatars.
// Assigned by member join order so no two members in a group share a color.
export const MemberColors = [
  '#007AFF', // blue
  '#FF3B30', // red
  '#34C759', // green
  '#FF9500', // orange
  '#AF52DE', // purple
  '#FF2D55', // pink
  '#5AC8FA', // light blue
  '#FFCC00', // yellow
  '#00C7BE', // teal
  '#FF6B35', // coral
  '#30D158', // mint
  '#BF5AF2', // violet
] as const;

export function memberColor(index: number): string {
  return MemberColors[index % MemberColors.length];
}
